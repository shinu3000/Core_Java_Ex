package coreJavaExample;

//A variable is a name which is associated with a value that can be changed.

public class Variable {

	public static void main(String[] args) {
		// i,str,bl are variable Name that holds the vales assigned to it 
		
		int i= 20; // variable is (i)
		String str="Hellow"; // variable is (str)
		boolean bl=false; // variable is (bl)
		 System.out.println("the int value of 'i' is : "+i);
		 System.out.println("the String value of 'str' is :"+str);
		 System.out.println("the boolean value of 'bl' is :"+bl);

	}

}
